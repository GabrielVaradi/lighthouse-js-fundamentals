var ingredients = ["eggs", "milk", "flour", "sugar", "baking soda", "baking powder", "chocolate chips", "bananas"];

var contentsWhile = 0
while (contentsWhile < ingredients.length) {
  console.log(ingredients[contentsWhile]);
  contentsWhile++;
}


for (var contents = 0 ; contents < ingredients.length ; contents++){
  console.log(ingredients[contents])
}



for (var backwards = 7 ; backwards > 0 ; backwards--){
  console.log(ingredients[backwards])
}


